fun main() {
    //While loop
    var number = 200
    while (number <= 205){
        println("Number is $number")
    number++}

    var num = 100
    while (num>= 95){
        println(num)
        num--
    }

    //Do...while loop
    var num1 = 20
    do {
        println(num1)
        num1++
    }while (num1 <= 25)

    //For loop
    var furniture = arrayOf("table","chair","desk")
    for (element in furniture){
        println(element)
    }

    var marks = arrayOf(85,86,77,45,80)
    for (mark in marks){
        println("you scored $mark")
    }

    for (numb in 30..35){
        println(numb)
    }

    for (letter in 'a'..'e'){
        println(letter)
    }










}













